// ===== DATE-BASED CONTENT SELECTOR =====
function getDailyContent(){
    const now=new Date();
    const dayOfYear=Math.floor((now-new Date(now.getFullYear(),0,0))/86400000);
    const monthDay=(now.getMonth()+1)+'-'+now.getDate();

    return{
        history:DAILY.history[monthDay]||DAILY.history["3-19"]||"Keep studying! Every day counts.",
        gk:DAILY.gk[dayOfYear%DAILY.gk.length],
        tip:DAILY.tips[dayOfYear%DAILY.tips.length],
        currentAffair:DAILY.currentAffairs[dayOfYear%DAILY.currentAffairs.length],
        word:DAILY.words[dayOfYear%DAILY.words.length],
        dayOfYear:dayOfYear
    };
}

